// This file defines an enumeration for possible toolbar positions in the browser.

namespace NavegadorWeb.Classes
{
    /// <summary>
    /// Represents the possible positions for the browser's toolbar.
    /// </summary>
    public enum ToolbarPosition
    {
        Top,    // Toolbar is at the top of the window
        Bottom, // Toolbar is at the bottom of the window
        Left,   // Toolbar is on the left side of the window
        Right   // Toolbar is on the right side of the window
    }
}
